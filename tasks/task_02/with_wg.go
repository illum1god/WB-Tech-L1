package main

import (
	"fmt"
	"sync"
)

func main() {
	// Исходный массив чисел
	numbers := []int{2, 4, 6, 8, 10}

	// Создаем WaitGroup для ожидания завершения всех горутин
	var wg sync.WaitGroup

	// Итерируемся по каждому числу в массиве
	for _, num := range numbers {
		// Увеличиваем счетчик WaitGroup перед запуском новой горутины
		wg.Add(1)

		// Запускаем горутину для вычисления квадрата числа
		go func(n int) {
			// Обозначаем завершение горутины
			defer wg.Done()

			// Вычисляем квадрат числа
			square := n * n

			// Выводим результат в stdout
			fmt.Printf("Квадрат числа %d равен %d\n", n, square)
		}(num) // Передаем текущий num как аргумент для избежания гонки данных
	}

	// Ожидаем завершения всех горутин
	wg.Wait()
}
