package main

import (
	"fmt"
	"time"
)

// Sleep приостанавливает выполнение программы на заданное количество миллисекунд.
func Sleep(milliseconds int) {
	// Создаем канал, который будет использоваться для блокировки выполнения.
	done := make(chan bool)

	// Запускаем горутину, которая будет ждать заданное количество времени.
	go func() {
		// Используем time.After для создания таймера.
		<-time.After(time.Duration(milliseconds) * time.Millisecond)
		// Отправляем сигнал в канал, чтобы разблокировать выполнение.
		done <- true
	}()

	// Блокируем выполнение до получения сигнала из канала.
	<-done
}

func main() {
	fmt.Println("Start")
	Sleep(2000) // Приостанавливаем выполнение на 2000 миллисекунд (2 секунды)
	fmt.Println("End")
}
